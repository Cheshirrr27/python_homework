# Создайте функцию dev_by_three, которая принимает один аргумент — число — и возвращает 'Да', если оно делится на три, и 'Нет'если не делится.
def dev_by_three(number):
    return "Да" if number % 3 == 0 else "Нет"

num = int(input("Введите число: "))                                  # код, который вызывает функцию и передает в нее число
result = dev_by_three(num)                                           # результат вызова функции должен сохраняться в переменную.
print(f"Делится ли на три {num}? - {result}")                        # Выведите в консоль сообщение в формате: Делится ли на три <число>? - Нет | Да

# Напишите функцию min_boxes которая принимает одно число — количество предметов — и возвращает мин кол-во коробок, для упаковки этих предметов, если 1 кор = 5 пред.

