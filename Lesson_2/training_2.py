# Напишите функцию check_divisibility, которая принимает одно число — n — и выводит все числа от 1 до n (включительно):

n = int(input ("Введите число: "))

def check_divisibility(n):
    for i in range(1, n + 1):
        if i % 4 == 0:                                                       # Если число делится на 2, но не на 4, оно выводится с текстом «Делится на 2, но не на 4».
            print(f"{i} - Делится и на 2, и на 4")
        elif i % 2 == 0:                                                     # Если число делится и на 2, и на 4, оно выводится с текстом «Делится и на 2, и на 4».
            print(f"{i} - Делится на 2, но не на 4")
        else:                                                                # Все остальные числа выводятся просто как есть.
            print(i)

check_divisibility(n)                                                        # запуск функции

# Напишите функцию quarter_of_year(), которая принимает один аргумент — номер месяца (от 1 до 12) — и возвращает номер квартала, к которому относится этот месяц.

def quarter_of_year(month):
    if 1 <= month <= 3:
        return "I квартал"
    if 4 <= month <= 6:
        return "II квартал"
    if 7 <= month <= 9:
        return "III квартал"
    if 10 <= month <= 12:
        return "IV квартал"
    return "Неверный номер месяца, используйте диапазон от 1 до 12 включительно"

month = int(input("Введите номер месяца (1-12): "))
print(quarter_of_year(month))

